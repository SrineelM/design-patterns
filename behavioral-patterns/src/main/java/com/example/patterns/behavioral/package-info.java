/**
 * Behavioral Design Patterns Package
 * 
 * This package contains implementations of all eleven Behavioral design patterns
 * from the Gang of Four. These patterns characterize the ways in which classes
 * or objects interact and distribute responsibility.
 * 
 * Patterns included:
 * - Chain of Responsibility: Pass request along chain of handlers
 * - Command: Encapsulate request as an object
 * - Iterator: Provide sequential access to elements
 * - Mediator: Encapsulate interaction between objects
 * - Memento: Capture and externalize object state
 * - Observer: Define one-to-many dependency
 * - State: Allow object to change behavior when state changes
 * - Strategy: Define family of interchangeable algorithms
 * - Template Method: Define algorithm skeleton
 * - Visitor: Represent operation on object structure
 * - Interpreter: Define grammar representation and interpreter
 * 
 * @author Design Patterns Team
 * @version 1.0.0
 * @since 1.0
 */
