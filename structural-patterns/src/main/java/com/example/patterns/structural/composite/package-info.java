/**
 * Composite Pattern Implementation
 * 
 * <p>The Composite pattern composes objects into tree structures to represent
 * part-whole hierarchies. Composite lets clients treat individual objects and
 * compositions of objects uniformly.
 * 
 * <p><b>Real-world Examples:</b>
 * <ul>
 *   <li>File systems (files and directories)</li>
 *   <li>Organization hierarchies (employees and departments)</li>
 *   <li>GUI component trees (containers and widgets)</li>
 *   <li>Menu structures (menu items and submenus)</li>
 * </ul>
 * 
 * <p><b>Key Components:</b>
 * <ul>
 *   <li>Component: Interface for objects in the composition</li>
 *   <li>Leaf: Represents leaf objects with no children</li>
 *   <li>Composite: Defines behavior for components having children</li>
 * </ul>
 * 
 * @since 1.0.0
 * @version 1.0.0
 */
