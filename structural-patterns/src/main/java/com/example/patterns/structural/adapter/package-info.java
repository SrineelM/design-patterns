/**
 * Adapter Pattern Implementation
 * 
 * <p>The Adapter pattern converts the interface of a class into another interface clients expect.
 * Adapter lets classes work together that couldn't otherwise because of incompatible interfaces.
 * 
 * <p><b>Real-world Examples:</b>
 * <ul>
 *   <li>Power adapters for international travel</li>
 *   <li>Card readers that allow different memory card formats</li>
 *   <li>Legacy system integration with modern applications</li>
 *   <li>MapStruct for bean-to-bean transformations</li>
 * </ul>
 * 
 * <p><b>Key Components:</b>
 * <ul>
 *   <li>Target: The interface clients use</li>
 *   <li>Adaptee: The existing interface needing adaptation</li>
 *   <li>Adapter: Converts Adaptee interface to Target interface</li>
 * </ul>
 * 
 * @since 1.0.0
 * @version 1.0.0
 */
